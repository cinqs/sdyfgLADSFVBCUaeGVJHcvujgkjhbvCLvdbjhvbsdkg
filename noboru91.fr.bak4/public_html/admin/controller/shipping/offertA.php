	private function validate() {
		if (!$this->user->hasPermission('modify', 'shipping/offertA')) {
			$this->error['warning'] = $this->language->get('error_permission');
		}
		
		if (!$this->error) {
			return true;
		} else {
			return false;
		}	
	}



